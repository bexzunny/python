# Operadores Aritméticos

a = 10
b = 3

# Suma +
suma = a + b
print(f"Suma: {suma}")

# Resta -
resta = a - b
print(f"Resta: {resta}")

# Multiplicación *
multiplicacion = a * b
print(f"Multiplicación: {multiplicacion}")

# División / (retorna tipo float)
division = a / b
print(f"División: {division:.2f}")

# División entera // (retorna tipo int)
div_entera = a // b
print(f"División entera: {div_entera}")

# Módulo % residuo
modulo = a % b
print(f"Módulo: {modulo}")

# Potencia **
potencia = a ** b
print(f"Potencia: {potencia}")
